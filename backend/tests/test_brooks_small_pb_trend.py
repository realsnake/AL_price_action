from services.strategy_engine import get_strategy


def _bar(ts: str, open_: float, high: float, low: float, close: float, volume: int = 1000) -> dict:
    return {
        "time": ts,
        "open": open_,
        "high": high,
        "low": low,
        "close": close,
        "volume": volume,
    }


def _trend_bars(entry_bar: dict) -> list[dict]:
    return [
        _bar("2025-01-06T14:30:00+00:00", 100.0, 100.6, 99.9, 100.4),
        _bar("2025-01-06T14:35:00+00:00", 100.4, 101.0, 100.3, 100.8),
        _bar("2025-01-06T14:40:00+00:00", 100.8, 101.4, 100.7, 101.2),
        _bar("2025-01-06T14:45:00+00:00", 101.2, 101.9, 101.1, 101.7),
        _bar("2025-01-06T14:50:00+00:00", 101.7, 102.4, 101.6, 102.1),
        _bar("2025-01-06T14:55:00+00:00", 102.1, 102.2, 101.8, 101.9),
        entry_bar,
        _bar("2025-01-06T15:05:00+00:00", 102.5, 102.7, 102.3, 102.4),
        _bar("2025-01-06T15:10:00+00:00", 102.4, 102.6, 102.2, 102.5),
    ]


def test_small_pullback_trend_emits_signal_for_strong_bull_breakout_bar():
    strategy = get_strategy(
        "brooks_small_pb_trend",
        {"ema_period": 3, "min_trend_bars": 3, "quantity": 1},
    )
    bars = _trend_bars(
        _bar("2025-01-06T15:00:00+00:00", 101.9, 102.9, 101.85, 102.7)
    )

    signals = strategy.generate_signals("QQQ", bars)

    assert len(signals) == 1
    assert signals[0].timestamp.isoformat() == "2025-01-06T15:00:00+00:00"


